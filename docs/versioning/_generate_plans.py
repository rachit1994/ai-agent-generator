def main(argv: list[str] | None = None) -> None:
    args = argv if argv is not None else sys.argv[1:]
    index_only = "--index-only" in args
    if index_only:
        if not PLANS.is_dir() or not any(PLANS.glob("*.md")):
            print("error: docs/versioning/plans/ is missing or empty; run full generator first", file=sys.stderr)
            raise SystemExit(2)
    else:
        emit_all_plans()
    n = write_plan_index()
    if index_only:
        print(f"Wrote INDEX.md only (--index-only); indexed {n} plans under {PLANS}")
    else:
        print(f"Wrote {n} plans under {PLANS} and INDEX.md")


if __name__ == "__main__":
    main()
