def plan(
    vid: str,
    slug: str,
    title: str,
    tier: str,
    section_ref: str,
    goal: str,
    prerequisites: str,
    in_scope: str,
    out_scope: str,
    deliverables: str,
    completion: str,
    verification: str,
    observability: str,
    documentation: str,
) -> str:
    return TEMPLATE.format(
        vid=vid,
        title=title,
        tier=tier,
        checklist=CHECKLIST,
        section_ref=section_ref,
        goal=goal,
        prerequisites=prerequisites,
        in_scope=in_scope,
        out_scope=out_scope,
        deliverables=deliverables,
        completion=completion,
        verification=verification,
        observability=observability,
        documentation=documentation,
    )


def bullet_list(lines: list[str]) -> str:
    return "\n".join(f"- [ ] {x}" for x in lines)


