def svc_plan(name: str, idx: int) -> tuple[str, str, dict]:
    slug = f"svc-{name.replace('_', '-')}"
    vid = f"OSV-SVC-{idx:02d}-{name}"
    title = f"{vid} — Service `{name}`"
    special = ""
    if name == "lifecycle-governance":
        special = "Sole owner of promotion/autonomy mutations."
    elif name == "identity-authz":
        special = "Sole issuer/validator of scoped approval tokens."
    elif name == "policy-management":
        special = "Sole activator of policy bundles."
    elif name == "memory-lifecycle":
        special = "Sole memory mutator."
    elif name == "safety-controller":
        special = "Sole final veto on high-risk actions."
    goal = f"Deploy `{name}` with api/runtime/tests/README, contract conformance, extraction-ready layout. {special}".strip()
    return (
        slug,
        title,
        {
            "tier": "2.2",
            "section": f"§4 ({name})",
            "goal": goal,
            "prereq": "OSV-CONTRACTS-01 + relevant contract families published; OSV-ORCH-01 for RPC callers.",
            "in_scope": f"Service `{name}` behavior per master architecture + operating-system-folder-structure.",
            "out": "Features owned by other services without RPC delegation.",
            "deliv": bullet_list(
                [
                    "`api/` stable import surface",
                    "`runtime/` process + config",
                    "`tests/` unit+integration",
                    "Service README with deploy + extract steps",
                    "Observability hooks (metrics/logs/traces)",
                ]
            ),
            "done": bullet_list(
                [
                    "Contract conformance suite green",
                    "Service passes OSV-EXTRACT-01 drill",
                    "SLOs defined (even if initial)",
                ]
            ),
            "verify": bullet_list(
                [
                    "CI job id documented",
                    "Load smoke optional for applicable services",
                ]
            ),
            "obs": f"`{name}` health + SLO dashboards.",
            "doc": f"Service README + ADR links.",
        },
    )


def emit(slug: str, title: str, d: dict) -> None:
    body = plan(
        vid=title.split("—")[0].strip(),
        slug=slug,
        title=title,
        tier=d["tier"],
        section_ref=d["section"],
        goal=d["goal"],
        prerequisites=d["prereq"],
        in_scope=d["in_scope"],
        out_scope=d["out"],
        deliverables=d["deliv"],
        completion=d["done"],
        verification=d["verify"],
        observability=d["obs"],
        documentation=d["doc"],
    )
    path = PLANS / f"{slug}.md"
    path.write_text(body, encoding="utf-8")


